"""Track S (#22) — S1(a): contrast prompt sets for safety-feature harvests.

DESIGN CONTRACT (so S2's two-block REML behavior_fit.rs gets a clean signal):
Each item is a MATCHED PAIR that isolates ONE behavior axis, holding topic /
length / surface form as fixed as possible so the activation DIFF between the two
legs is the behavior direction, not a topic/format confound.

Per item:
  - `context`:  the shared setup (identical or minimally-varied across the pair).
  - `pos`:      the leg that ELICITS the behavior (e.g. a sycophancy-inviting frame).
  - `neg`:      the matched leg that does NOT (neutral / opposite frame).
  - `probe`:    a short continuation whose log-prob MEASURES the behavior. The
                behavior-nats target for S2 is
                    Y = logP(probe_behavioral | leg) − logP(probe_control | leg)
                i.e. how much each leg tilts the model toward the behavioral
                continuation vs its honest/neutral counterpart. `probe` carries
                both continuations: {"behavioral": ..., "control": ...}.

So the harvest emits, per leg: hidden activations at the cached layers (the X block
for reconstruction) AND the two probe log-probs (→ the nats-unit Y block). The
behavior is defined by what it DOES to Y, in nats — never by a hand-labeled name.

Honesty scope (per #22): these are AUDIT probes — measuring whether a behavior is
present and how a feature drives it, for a monitor that FLAGS. Refusal is tracked
as a two-sided axis (over- AND under-refusal are both alignment failures), not as
a thing to maximize.

This is v0 — a representative, expandable seed set (≈8 pairs/behavior here; the
harvest wants ≫ that, generated by the templates below). Schema-stable so the
harvest pipeline can consume it directly once we confirm its input format.
"""

