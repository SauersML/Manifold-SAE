"""LLM-like stress test for persistent-curve SAE.

Synthetic data that mimics the structure expected in LLM residual streams:
  * High ambient dim (D = 512).
  * Many features (F_gt = 64).
  * Mixed atom types: about half are POINT atoms (direction-only, like
    standard SAE features) and about half are CURVE atoms (continuous
    1D family along a smooth path).
  * Sparse activation (~ 5 active per token).
  * Realistic amplitude distribution (heavy-tail).

Trains:
  * persistent-curve SAE (this repo)
  * vanilla SAE baseline (linear encoder + linear decoder + L1)

Compares:
  * Reconstruction MSE
  * For each GT CURVE feature: how many vanilla-SAE dictionary atoms are
    used to represent it (proxy: how many vanilla SAE features co-fire
    along the curve) vs the curve SAE.

If the curve SAE represents each continuous family as a SINGLE feature
while the vanilla SAE needs many to span the same family, that's the
LLM-applicability win.
"""

from __future__ import annotations

import json
import sys
import time
from dataclasses import asdict, dataclass
from pathlib import Path

import numpy as np
import torch
from torch import nn
from torch.utils.data import DataLoader, TensorDataset


@dataclass(frozen=True)
class Config:
    d_ambient: int = 512
    n_point_features: int = 32
    n_curve_features: int = 32
    n_samples: int = 50_000
    sparsity: float = 5.0 / 64           # ~5 active per token
    noise: float = 0.02
    curve_amp_low: float = 0.5
    curve_amp_high: float = 1.5
    point_amp_low: float = 0.5
    point_amp_high: float = 1.5
    n_curve_anchors: int = 64           # GT curves sampled at this many points

    # Both SAEs:
    sae_features: int = 192             # overcomplete (3x GT)
    n_steps: int = 4000
    batch_size: int = 256
    lr: float = 1e-3

    # Curve SAE specific:
    n_basis: int = 8
    top_k: int = 8                      # sparser than data → forces dictionary use
    intrinsic_rank: int = 4
    sparsity_weight: float = 1e-3
    ortho_weight: float = 1e-3
    smoothness_weight: float = 1e-4

    seed: int = 0
    output_dir: str = "runs/LLM_LIKE_STRESS"


# ---------------------------------------------------------------------------
# Synthetic generator
# ---------------------------------------------------------------------------

def _smooth_random_curve(D: int, n_anchors: int, rng: np.random.Generator) -> np.ndarray:
    """A smooth random curve in R^D, generated by passing white-noise
    through a low-pass filter (cumsum then center)."""
    raw = rng.standard_normal((n_anchors, D)) * 0.3
    smooth = np.cumsum(raw, axis=0)
    smooth -= smooth.mean(axis=0, keepdims=True)
    # Normalize to roughly unit Frobenius
    smooth /= max(np.linalg.norm(smooth) / (n_anchors ** 0.5), 1e-8)
    return smooth


def make_data(cfg: Config) -> dict:
    rng = np.random.default_rng(cfg.seed)
    F_point = cfg.n_point_features
    F_curve = cfg.n_curve_features
    F = F_point + F_curve

    point_dirs = rng.standard_normal((F_point, cfg.d_ambient))
    point_dirs /= np.linalg.norm(point_dirs, axis=1, keepdims=True) + 1e-8

    curves = np.stack([_smooth_random_curve(cfg.d_ambient, cfg.n_curve_anchors, rng) for _ in range(F_curve)])  # (F_curve, T, D)

    # Activation pattern: Bernoulli per (sample, feature)
    p = cfg.sparsity
    active = rng.random((cfg.n_samples, F)) < p
    # Amplitudes
    point_amps = rng.uniform(cfg.point_amp_low, cfg.point_amp_high, (cfg.n_samples, F_point))
    curve_amps = rng.uniform(cfg.curve_amp_low, cfg.curve_amp_high, (cfg.n_samples, F_curve))
    # Positions for curve features (random in [0, T-1])
    curve_pos = rng.integers(0, cfg.n_curve_anchors, (cfg.n_samples, F_curve))

    X = np.zeros((cfg.n_samples, cfg.d_ambient), dtype=np.float32)
    for k in range(F_point):
        mask = active[:, k]
        if mask.any():
            X[mask] += (point_amps[mask, k, None] * point_dirs[k][None, :]).astype(np.float32)
    for k in range(F_curve):
        mask = active[:, F_point + k]
        if mask.any():
            idx = curve_pos[mask, k]
            contrib = curve_amps[mask, k, None] * curves[k][idx]  # (n_active, D)
            X[mask] += contrib.astype(np.float32)
    X += rng.standard_normal(X.shape).astype(np.float32) * cfg.noise

    return {
        "X": torch.from_numpy(X),
        "point_dirs": point_dirs,
        "curves": curves,
        "active": active,
        "curve_pos": curve_pos,
    }


# ---------------------------------------------------------------------------
# Vanilla SAE baseline
# ---------------------------------------------------------------------------

class VanillaSAE(nn.Module):
    def __init__(self, D: int, F: int, top_k: int) -> None:
        super().__init__()
        self.D = D; self.F = F; self.top_k = top_k
        self.norm = nn.LayerNorm(D)
        self.W_enc = nn.Linear(D, F, bias=True)
        self.W_dec = nn.Parameter(torch.randn(F, D) * (1.0 / D ** 0.5))

    def forward(self, x):
        h = self.W_enc(self.norm(x))
        h = torch.nn.functional.relu(h)
        # TopK gate
        if self.top_k < self.F:
            vals, idx = torch.topk(h, self.top_k, dim=1)
            gate = torch.zeros_like(h)
            gate.scatter_(1, idx, vals)
            z = gate
        else:
            z = h
        recon = z @ self.W_dec
        return recon, z


def train_vanilla(X: torch.Tensor, cfg: Config, device: torch.device) -> tuple[VanillaSAE, dict]:
    sae = VanillaSAE(D=X.shape[1], F=cfg.sae_features, top_k=cfg.top_k).to(device)
    opt = torch.optim.Adam(sae.parameters(), lr=cfg.lr)
    ds = TensorDataset(X)
    loader = DataLoader(ds, batch_size=cfg.batch_size, shuffle=True, drop_last=True)
    it = iter(loader)
    step = 0
    history = {"step": [], "mse": []}
    while step < cfg.n_steps:
        try:
            (batch,) = next(it)
        except StopIteration:
            it = iter(loader); (batch,) = next(it)
        batch = batch.to(device)
        opt.zero_grad(set_to_none=True)
        recon, z = sae(batch)
        mse = torch.mean((recon - batch) ** 2)
        l1 = z.abs().mean()
        loss = mse + 1e-3 * l1
        loss.backward()
        torch.nn.utils.clip_grad_norm_(sae.parameters(), 1.0)
        opt.step()
        if step % max(cfg.n_steps // 10, 1) == 0:
            history["step"].append(step); history["mse"].append(float(mse.item()))
            print(f"  [vanilla step {step:5d}] mse={mse.item():.4e}")
        step += 1
    return sae, history


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main(cfg: Config = Config()) -> int:
    torch.manual_seed(cfg.seed); np.random.seed(cfg.seed)
    out_dir = Path(cfg.output_dir); out_dir.mkdir(parents=True, exist_ok=True)
    # CPU only — gamfit Duchon basis is float64; MPS lacks float64 support.
    device = torch.device("cpu")
    print(f"[setup] device={device}")

    print("[data] generating LLM-like synthetic dataset")
    data = make_data(cfg)
    X = data["X"]
    print(f"[data] X shape: {tuple(X.shape)}  point_features={cfg.n_point_features}  curve_features={cfg.n_curve_features}")

    # Standard normalization
    mu = X.mean(dim=0, keepdim=True)
    sigma = (X - mu).std().item()
    X_n = (X - mu) / max(sigma, 1e-6)

    # Vanilla SAE
    print("[vanilla] training vanilla SAE baseline")
    t0 = time.time()
    vanilla_sae, vh = train_vanilla(X_n, cfg, device)
    t_vanilla = time.time() - t0

    vanilla_sae.eval()
    with torch.no_grad():
        eb = X_n[: min(4096, X_n.shape[0])].to(device)
        recon, z = vanilla_sae(eb)
        mse_v = float(torch.mean((recon - eb) ** 2).item())
        alive_v = ((z > 0).any(dim=0)).sum().item()
        var_v = float(eb.var().item())
    print(f"[vanilla] MSE={mse_v:.4e} var={var_v:.4e} explained={1 - mse_v/var_v:.3f} alive={alive_v}/{cfg.sae_features}")

    # Curve SAE
    print("[curve] training persistent-curve SAE")
    from manifold_sae.sae import ManifoldSAE, ManifoldSAEConfig
    from manifold_sae.losses import total_loss
    sae_cfg = ManifoldSAEConfig(
        input_dim=X.shape[1], n_features=cfg.sae_features, n_basis=cfg.n_basis,
        top_k=cfg.top_k, intrinsic_rank=cfg.intrinsic_rank,
        sparsity_weight=cfg.sparsity_weight, cumulant_weight=0.0,
        ortho_weight=cfg.ortho_weight, smoothness_weight=cfg.smoothness_weight,
        encoder_type="linear",
    )
    curve_sae = ManifoldSAE(sae_cfg).to(device)
    n_p = sum(p.numel() for p in curve_sae.parameters())
    print(f"[curve] params: {n_p/1e6:.2f}M")
    opt = torch.optim.Adam(curve_sae.parameters(), lr=cfg.lr)
    ds = TensorDataset(X_n)
    loader = DataLoader(ds, batch_size=cfg.batch_size, shuffle=True, drop_last=True)
    it = iter(loader); step = 0
    ch = {"step": [], "mse": []}
    t1 = time.time()
    while step < cfg.n_steps:
        try:
            (batch,) = next(it)
        except StopIteration:
            it = iter(loader); (batch,) = next(it)
        batch = batch.to(device)
        opt.zero_grad(set_to_none=True)
        out = curve_sae(batch)
        losses = total_loss(out, batch, sae_cfg)
        losses["total"].backward()
        torch.nn.utils.clip_grad_norm_(curve_sae.parameters(), 1.0)
        opt.step()
        if step % max(cfg.n_steps // 10, 1) == 0:
            ch["step"].append(step); ch["mse"].append(float(losses["mse"].item()))
            print(f"  [curve step {step:5d}] mse={losses['mse'].item():.4e}")
        step += 1
    t_curve = time.time() - t1

    curve_sae.eval()
    with torch.no_grad():
        eb = X_n[: min(4096, X_n.shape[0])].to(device)
        out = curve_sae(eb)
        mse_c = float(torch.mean((out.reconstruction - eb) ** 2).item())
        alive_c = ((out.amplitudes > 0.5).any(dim=0)).sum().item()
    print(f"[curve] MSE={mse_c:.4e} explained={1 - mse_c/var_v:.3f} alive={alive_c}/{cfg.sae_features}")

    # Family-coverage diagnostic: for each GT CURVE feature, count how
    # many SAE dictionary atoms co-fire on tokens where that GT feature
    # is active. A curve SAE should cover a curve family with ~1 atom.
    # A vanilla SAE should need many.
    print("[diag] computing per-GT-curve dictionary cost")
    active = data["active"]  # (N, F_total) numpy
    F_point = cfg.n_point_features
    sample_size = min(8192, X_n.shape[0])
    eb = X_n[:sample_size].to(device)
    with torch.no_grad():
        _, z_v = vanilla_sae(eb)
        out_c = curve_sae(eb)
    z_v_np = (z_v > 0).cpu().numpy()
    z_c_np = (out_c.amplitudes > 0.5).cpu().numpy()
    coverage_vanilla, coverage_curve = [], []
    for k in range(cfg.n_curve_features):
        mask = active[:sample_size, F_point + k]
        if mask.sum() < 20:
            continue
        # Distinct SAE atoms that fire in at least 2% of "GT-curve-k active"
        # tokens — measures the dictionary spread used to represent curve k.
        # A curve SAE that has one atom per GT curve should see ~1; a vanilla
        # SAE that breaks each curve into position-bins should see many more.
        v_freq = z_v_np[mask].mean(axis=0)
        c_freq = z_c_np[mask].mean(axis=0)
        coverage_vanilla.append(int((v_freq > 0.02).sum()))
        coverage_curve.append(int((c_freq > 0.02).sum()))
    cv_mean = float(np.mean(coverage_vanilla)) if coverage_vanilla else 0.0
    cc_mean = float(np.mean(coverage_curve)) if coverage_curve else 0.0
    print(f"[diag] avg atoms used per GT curve family: vanilla={cv_mean:.2f}  curve_sae={cc_mean:.2f}")

    report = {
        "config": asdict(cfg),
        "vanilla": {"mse": mse_v, "var": var_v, "explained": 1 - mse_v/var_v, "alive": alive_v, "train_seconds": t_vanilla, "history": vh},
        "curve": {"mse": mse_c, "explained": 1 - mse_c/var_v, "alive": alive_c, "train_seconds": t_curve, "history": ch, "params_M": n_p/1e6},
        "coverage": {
            "vanilla_atoms_per_curve_family_mean": cv_mean,
            "curve_atoms_per_curve_family_mean": cc_mean,
            "vanilla_per_curve": coverage_vanilla,
            "curve_per_curve": coverage_curve,
        },
    }
    (out_dir / "results.json").write_text(json.dumps(report, indent=2, default=float))
    print(f"[done] wrote {out_dir / 'results.json'}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
