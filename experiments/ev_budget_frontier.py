"""EV-vs-budget frontier + manifold/linear parity table (W10).

Two deliverables for the manifold-SAE evaluation suite:

  1. PARITY TABLE -- manifold-SAE vs gamfit linear-SAE baseline at MATCHED
     PARAMETER BUDGET. Honest parameter accounting: a curved (circle, d=1) atom
     with ``b`` basis functions over ``p`` ambient dims costs its full chart
     parameterization ``b*p``; a linear/collapsed atom costs ``p``. For each
     manifold fit we read its realized budget Theta (curved atoms at b*p, atoms
     the fit collapsed to linear at p) and give the linear baseline the SAME
     budget: ``K_lin = round(Theta / p)`` rank-1 atoms. The question answered is
     "at equal parameters, does curvature buy held-out EV?".

  2. EV-vs-BUDGET FRONTIER -- for one generous-K manifold fit, the per-curved-atom
     ``(Theta_k, dEV_k)`` frontier. ``dEV_k`` is each atom's MARGINAL held-out
     leave-one-atom-out EV contribution (computed here on the held-out split from
     the gated per-atom decode), cross-checked against the FFI's in-sample
     ``hybrid_split.train_loao_delta_ev``. Sorting the atoms by marginal dEV shows
     which curved atoms pay rent and where the evidence-limited K cutoff sits --
     the doctrine "keep curved K evidence-limited and small".

DATA PROVENANCE
  Real OLMo-3-32B activations ARE present locally (runs/OLMO3_32B_*_SELF_QUALIA_*/
  activations.npy, L44). They were tried first, but that self/qualia last-token
  readout is a *linear axis* representation: circle atoms co-collapse
  ("no atom carries material signal to anchor", held-out EV ~0.03-0.11), and the
  documented *curved* structure (the color/hue loop, DATA_README sec 8) is not
  present locally as an array harvest. So the frontier -- which needs KNOWN curved
  ground truth to locate where marginal dEV crosses noise -- is run on fixed,
  real-shaped PLANTED synthetic: ``n_curved`` circle atoms + ``n_linear`` rank-1
  atoms in disjoint orthogonal subspaces, then a random rotation Q mixes the
  coordinates (realistic; not axis-aligned). Ground truth curved count is known.
  The real-data co-collapse is itself reported (see ``--real-probe``) and is
  consistent evidence for the doctrine: curved structure is rare / evidence-limited
  in real residual streams.

BUILD NOTE (shared tree)
  The compiled gamfit extension in this .venv predates two kwargs the Python
  wrapper passes (``structured_residual_passes`` / ``promote_from_residual``); both
  default to 0/False so we runtime-strip them in ``_install_rust_shim`` (no gam
  files are edited). The REML manifold solver in this build is also slow and
  memory-leaky in its inner Newton loop, so every manifold fit runs in an isolated
  subprocess with a wall-clock timeout; a fit that OOMs/times out is recorded as a
  miss rather than crashing the sweep.

Usage
  python ev_budget_frontier.py            # run the full suite -> frontier_out/
  python ev_budget_frontier.py --worker <cfg.json> <out.json>   # internal
  python ev_budget_frontier.py --real-probe   # quick real-activation co-collapse probe
"""

