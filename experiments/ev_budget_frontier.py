def real_probe():
    """Quick honest probe: fit circle atoms to REAL OLMo L44 self/qualia and show
    the held-out EV / co-collapse. Small K, short -- just to document the finding."""
    import numpy as np
    import gamfit
    _install_rust_shim()
    path = ("/Users/user/Manifold-SAE/runs/OLMO3_32B_INSTRUCT_SELF_QUALIA_LAST/"
            "activations.npy")
    A = np.load(path, mmap_mode="r")
    H = np.asarray(A[:, 44, :], dtype=np.float64)
    rng = np.random.default_rng(0)
    H = H[rng.permutation(len(H))]
    nt = int(0.2 * len(H))
    test, train = H[:nt], H[nt:]
    mu = train.mean(0)
    tr, te = train - mu, test - mu
    _, _, Vt = np.linalg.svd(tr, full_matrices=False)
    Vt = Vt[:16]
    tr, te = tr @ Vt.T, te @ Vt.T
    sd = tr.std(0) + 1e-8
    tr, te = tr / sd, te / sd
    print(f"[real] OLMo-3-32B L44 self/qualia, PCA p=16, train={tr.shape} test={te.shape}")
    fit = gamfit.sae_manifold_fit(tr, K=4, d_atom=1, atom_topology="circle", n_iter=6)
    print(f"[real] circle-atom held-out EV = {ev(te, np.asarray(fit.reconstruct(te))):.3f} "
          f"(co-collapse expected: this readout is a linear axis, not a loop)")


